package com.netflix.explorers.rest;

public class RestKey {
    private String version;
    private String name;
    
    public RestKey(String version, String name) {
        this.name    = name;
        this.version = version;
    }
    
    @Override
    public String toString() {
        return "RestKey [version=" + version + ", name=" + name + "]";
    }


    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((version == null) ? 0 : version.hashCode());
        return result;
    }


    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        RestKey other = (RestKey) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (version == null) {
            if (other.version != null)
                return false;
        } else if (!version.equals(other.version))
            return false;
        return true;
    }

    public String getVersion() {
        return version;
    }

    public String getName() {
        return name;
    }
}
